; gfxctx.asm - paragraph-aligned storage for the engine graphics-context para
; block (struct GraphicsContext, DGROUP:0x2010 in KRONDOR.EXE). Hand-maintained;
; field layout mirrors INCLUDE\gfxctx.inc / structs.h.
;
; A `para public 'DATA'` segment GROUPed into DGROUP forces _g_graphics_context to
; a 16-byte boundary: the VGA driver's scratch overlay (scratch_DS = DGROUP +
; offset(g_text_fg_color)/16) SHR-by-4s the base, truncating the low nibble, so a
; non-paragraph base would skew every scratch:[field] access. The block stays ONE
; contiguous run so its interior addresses are fixed; the carved-out screen-dim
; words and render tail are PUBLIC interior labels of it.
;
; Alignment is a per-SEGMENT OMF attribute: Borland C++ word-aligns ALL DGROUP
; data (every C _DATA/_BSS SEGDEF is align=word), so only a SEGMENT PARA directive
; produces the paragraph boundary this struct needs. The VGA driver (VMCODE.OVL
; vga_init) SHR-4s the base video_init pushes (VIDINIT.ASM +0x6a: offset
; _g_graphics_context = 0x2010, low nibble 0) so scratch_DS:0 lands on the struct;
; a non-para base would skew every scratch:[field]. The para alignment also forces
; the 8-byte pad at 0x2008.

