// Dialog graph (DDX) data layer for the editor.
//
// Betrayal at Krondor's dialog system is a scripting VM: every scene bark, shop
// transaction, NPC conversation, cipher puzzle and party-stat change is a DDX
// bytecode record. Records reference each other by key (bit-31 set) or by raw
// file offset (bit-31 clear) to form a graph. This module parses ALL 32
// DIAL_Z<nn>.DDX partitions into a single graph keyed by (fileNumber, offset)
// and layers component/root/decoder services on top for the Phase-B graph UI.
//
// Node identity = (fileNumber, offset). Directory keys are ALIASES onto nodes.
// The census this module reproduces: 8184 nodes, 8452 edges, 1250 components,
// giant = 5362 nodes.

