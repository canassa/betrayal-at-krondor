/** Return a short human name for a gstate event id (docs §5). */
export function stateName(id) {
  const v = id >>> 0;
  if (v in STATE_7530_NAMES) return STATE_7530_NAMES[v];
  if (v >= 0x7544 && v <= 0x754c) return `is-chapter-${v - 0x7544 + 1}`;
  if (v >= 7500 && v <= 8491) return `keyword-asked ${v - 7500}`;
  if (v >= 56000 && v % 10 === 0) return `bitmap-row ${(v - 56000) / 10}`;
  if (v >= 56000) return `chapter-flag byte ${(v - 56000) / 10 | 0}`;
  if (v >= 7000 && v <= 7259) return `door-open ${v - 7000}`;
  if (v >= 6700 && v <= 6999) return `topic-inhibited ${v - 6700}`;
  if (v >= 6480 && v <= 6499) return `town-visited ${v - 6480}`;
  if (v >= 5220) return `encounter-fought ${v - 5220}`;
  if (v >= 400 && v <= 5199) return `hotspot-done ${v - 400}`;
  if (v === 0) return 'always';
  return `flag ${hex(v)}`;
}

